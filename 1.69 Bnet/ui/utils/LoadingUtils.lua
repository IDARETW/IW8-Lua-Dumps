function LOADING.GetHintGameTypes(arg_1_0)
	local var_1_0 = Dvar.DHEEJCCJBH("MOLPOSLOMO")
	local var_1_1 = Dvar.DHEEJCCJBH("MTQQTNNTMM") or ""
	local var_1_2 = Dvar.DHEEJCCJBH("scr_br_gametype")
	local var_1_3 = Dvar.DHEEJCCJBH("NSQLTTMRMP")

	if CONDITIONS.IsWZPayload() then
		if Engine.DDJFBBJAIG() then
			var_1_0 = {
				"payload_attackers",
				"payload_defenders"
			}
		else
			local var_1_4 = Game.DHEDEIHGF()
			local var_1_5 = DataSources.inGame.MP.match.currentRound ~= nil and DataSources.inGame.MP.match.currentRound:GetValue(arg_1_0) or 0

			if var_1_5 == 0 and var_1_4 == Teams.axis or var_1_5 == 1 and var_1_4 == Teams.allies then
				var_1_0 = "payload_attackers"
			else
				var_1_0 = "payload_defenders"
			end
		end
	elseif CONDITIONS.IsBRRevealMode() then
		var_1_0 = "reveal"
	elseif CONDITIONS.IsBRX2Gametype() then
		var_1_0 = "x2"
	elseif CONDITIONS.IsBRLEPGametype() then
		var_1_0 = "lep"
	elseif CONDITIONS.IsBRExfiltrationMode() then
		var_1_0 = "exfiltration"
	elseif CONDITIONS.IsRebirthSoloGametype() then
		var_1_0 = "rebirthsolo"

		if CONDITIONS.IsMapDragonsDen() then
			var_1_0 = "ddresurgenceSolo"
		end
	elseif CONDITIONS.IsWZRatRace() then
		var_1_0 = "ratrace"
	elseif CONDITIONS.IsBRHalloweenPublicMatch() and var_1_2 ~= "zxp" then
		var_1_0 = "seasonalEvent"
	elseif CONDITIONS.IsBRGhosts() then
		var_1_0 = "gxp"
	elseif CONDITIONS.IsVOVMode() then
